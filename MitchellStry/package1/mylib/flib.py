import math
def f1(x):
    """
    Linear function

    Args:
        x (float): argument
    
    Returns:
        (float): linear function of x
    """
    return x;

def f2(x):
    """
    Square function

    Args:
        x (float): argument
    
    Returns:
        (float): Square function of x
    """
    return x**2;

def f3(x):
    """
    Sin function
    
    Args:
         x (float): argument
    
    Returns:
        (float): sin function of x
    """
    return (math.sin(x))**2;

