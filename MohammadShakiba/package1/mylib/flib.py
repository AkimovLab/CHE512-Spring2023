import math

def f1(x):
    """
    Linear function

    Args:
        x (float): The value

    Returns:
        x (float): Linear function of x
    """
    return x

def f2(x):
    """
    Square function

    Args:
        x (float): The value

    Returns:
        x**2 (float): Square of x
    """
    return x**2

def f3(x):
    """
    Sine function

    Args:
        x (float): The value

    Returns:
        x (float): Sine function of x
    """
    return (math.sin(x))**2



